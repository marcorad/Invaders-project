package util;

/**Specifies a pair of min and max numbers for code clarity
 * @author Marco
 *
 */
public class MinMaxPair {
	public float min, max;

	/**
	 * @param min The minimum
	 * @param max The maximum
	 */
	public MinMaxPair(float min, float max) {
		this.min = min;
		this.max = max;
	}
}
